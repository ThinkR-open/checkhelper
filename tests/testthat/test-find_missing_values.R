path <- suppressWarnings(create_example_pkg())

test_that("find_missing_tags works", {
  if (packageVersion("roxygen2") > "7.1.2") {
    expect_warning(
      out <- expect_message(
        find_missing_tags(path),
        "my_long_fun_name_for_multiple_lines_globals"
      ),
      regexp = "@return"
    )
  } else {
    out <- expect_message(
      find_missing_tags(path),
      "my_long_fun_name_for_multiple_lines_globals"
    )
  }

  expect_equal(nrow(out), 5)
  expect_equal(out$filename, rep("function.R", 5))
  expect_equal(
    out$topic,
    c(
      "my_long_fun_name_for_multiple_lines_globals", "my_plot",
      "my_not_exported_doc", "my_not_exported_nord", "my_plot_rdname"
    )
  )
  expect_false("my_not_exported_nodoc" %in% out$topic)
  expect_equal(out$has_export, c(TRUE, TRUE, FALSE, FALSE, TRUE))
  # last is true because of rdname tag

  if (packageVersion("roxygen2") > "7.1.2") {
    expect_equal(
      out$has_return,
      c(FALSE, TRUE, FALSE, FALSE, TRUE)
    )
  } else {
    expect_equal(
      out$has_return,
      c(TRUE, TRUE, FALSE, FALSE, TRUE)
    )
  }
  expect_equal(
    out$has_nord,
    c(FALSE, FALSE, FALSE, TRUE, FALSE)
  )
  expect_equal(
    out$test_has_export_and_return,
    c("not_ok", "ok", "ok", "ok", "ok")
  )
  expect_equal(
    out$test_has_export_or_has_nord,
    c("ok", "ok", "not_ok", "ok", "ok")
  )

  withr::with_dir(path, {
    usethis::use_package_doc()
    usethis::use_pipe()
    usethis::use_data(iris)
    use_data_doc("iris")

    cat(
      "
#' title
#' @param x x
#' @export
#' @return something
the_function <- function(x) x

#' @rdname the_function
#' @inheritParams the_function
#' @export
the_alias <- the_function

#' @rdname the_function
#' @inheritParams the_function
the_alias2 <- the_function

#' @rdname the_function
#' @inheritParams the_function
#' @noRd
the_alias3 <- the_function",
      file = "R/function.R",
      append = TRUE
    )

    cat(
      "
#' title
#' @param x x
#' @export
#' @return something
the_other_function <- function(x) x

#' @rdname the_other_function
#' @inheritParams the_other_function
#' @export
the_other_alias <- the_other_function

#' @rdname the_other_function
#' @inheritParams the_other_function
the_other_alias2 <- the_other_function

#' @rdname the_other_function
#' @inheritParams the_other_function
#' @noRd
the_other_alias3 <- the_other_function",
      file = "R/function2.R"
    )

    expect_warning(
      attachment::att_amend_desc(),
      regexp = "@return"
    )
  })

  if (packageVersion("roxygen2") > "7.1.2") {
    expect_warning(
      out <- expect_message(
        find_missing_tags(path),
        "my_long_fun_name_for_multiple_lines_globals"
      ),
      regexp = "@return"
    )
  } else {
    out <- expect_message(
      find_missing_tags(path),
      "my_long_fun_name_for_multiple_lines_globals"
    )
  }

  false_positive <- out %>%
    dplyr::filter(
      filename %in% c("checkpackage-package.R", "doc_iris.R", "utils-pipe.R")
    )

  expect_equal(nrow(false_positive), 3) # 3 false positives

  expect_equal(
    false_positive$has_export,
    c(FALSE, FALSE, TRUE)
  )

  expect_equal(
    false_positive$has_nord,
    c(FALSE, FALSE, FALSE)
  )

  expect_equal(
    false_positive$test_has_export_and_return,
    c("ok", "ok", "ok")
  )

  expect_equal(
    false_positive$test_has_export_or_has_nord,
    c("not_ok", "not_ok", "ok")
  )
})

# Remove path
unlink(path, recursive = TRUE)
